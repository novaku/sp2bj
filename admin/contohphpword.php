<p><strong>H.WAWAN DISMAWAN,ST.,S.Sos.,MM</strong><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong>: &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kuasa  Pengguna Anggaran (KPA) Selaku Pejabat Pembuat Komitmen di Lingkungan Dinas  Bina Marga dan Pengairan Kabupaten Karawang Tahun Anggaran 2014, berdasarkan Surat Keputusan Bupati Karawang No. 903/KEP.876-Huk/2013 tanggal 30 Desember 2013<strong> </strong>bertindak untuk dan atas nama  Pemerintah Kabupaten Karawang yang berkedudukan di Jalan Kertabumi No. 31  Karawang selanjutnya disebut <strong><u>PIHAK  KESATU</u></strong>.<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;DAN<br />
  <strong>DARIJAN HARISMAN</strong><strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong>: &nbsp;&nbsp; Direktur <strong>CV. KARYA CIPTA MANDIRI</strong><strong> </strong>&nbsp;Dalam hal ini  bertindak untuk dan atas nama <strong>CV. KARYA CIPTA MANDIRI</strong>&nbsp; yang Anggaran  Dasarnya dimuat dalam akta tanggal <strong>17 Oktober 1996</strong> Nomor <strong>416</strong> yang dibuat dihadapan Notaris <strong>Hj. ROSIDA  SURYANA, SH.</strong><strong> </strong>yang berkedudukan di <strong>Jl. Dahlia No.7 Guro II Karawang</strong><strong> </strong>sebagai pelaksana kegiatan selanjutnya disebut <strong><u>PIHAK KEDUA</u></strong>. </p>
<p>Bahwa para pihak telah sepakat untuk mengadakan  Perjanjian Kerja dalam rangka pelaksanaan kegiatan <strong>Pembangunan Infrastruktur  Perdesaan Peningkatan Jalan Dusun Kamojing Timur Desa Kamojing Kecamatan  Cikampek</strong> <strong>Pj = </strong><strong>198</strong><strong> </strong><strong>M'</strong><strong>, Lb = </strong><strong>3,00</strong><strong> M'</strong> di Kecamatan <strong>Cikampek</strong>.<strong></strong></p>
<p>PIHAK KEDUA bertindak sebagaimana tersebut adalah sebagai  pelaksana dan bertanggung jawab atas pelaksanaan kegiatan <strong>Pembangunan Infrastruktur  Perdesaan Peningkatan Jalan Dusun Kamojing Timur Desa Kamojing Kecamatan  Cikampek</strong> <strong>Pj = </strong><strong>198</strong><strong> M'</strong><strong>, Lb = </strong><strong>3,00</strong><strong> M'</strong> di Kecamatan <strong>Cikampek</strong>.</p>
